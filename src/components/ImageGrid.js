export default function CheckboxListSecondary() {
  const [checked, setChecked] = React.useState([1]);
  const [page, setPage] = React.useState('default');

  window.onload = function() {
    setPage('default');
    console.log("1. " + page);
  };

  const handleToggle = (value) => () => {
    const currentIndex = checked.indexOf(value);
    const newChecked = [...checked];

    if (currentIndex === -1) {
      newChecked.push(value);
    } else {
      newChecked.splice(currentIndex, 1);
    }

    // setChecked(newChecked);
    setChecked(Array.apply(0, Array(30 - 1)).map((element, index) => index + 0));
  };

  const backOneStep = (value) => {
    setPage(value);
  };

  var list = []
  for(var i=0; i<30; i++) {
    list.push(i);
  }

  const submitForm = (event) => {
    setPage(event);
  }

  var output = '';
  if(page == 'show_captions') {
      output = <div><Button variant="contained" onClick={(event) => submitForm("show_text")}>Submit Images to generate Captions</Button>
        <Button variant="contained" onClick={(event) => backOneStep('show_images')}>Back</Button><ImageList sx={{ width: '100%' }} cols={1} rowHeight={680}>
      {checked.map((value) => {
          const labelId = `checkbox-list-secondary-label-${value}`;
          return (
              <ImageListItem
                key={value + '-checked'}
              >
                <ListItemButton>
                  <img
                    src={`${paths[value].replace("\\", "/")}`}
                    srcSet={`${paths[value].replace("\\", "/")}`}
                    loading="lazy"
                  />
                  <ListItemText style={{"padding-left": "40px"}}>{captions[value]}</ListItemText>
                </ListItemButton>
              </ImageListItem>
          );
        })}
    </ImageList></div>;
  }

  if (page == 'show_images' || page == 'default') {
    output = <div><Button variant="contained" onClick={(event) => submitForm("show_captions")}>Submit Images to generate Captions</Button><ImageList sx={{ width: '100%' }} cols={2} rowHeight={680}>
    {list.map((value) => {
      const labelId = `checkbox-list-secondary-label-${value}`;
      return (
          <ImageListItem
            key={value + '-select'}
            onClick={handleToggle(value)}
          >
            <Checkbox
                edge="start"
                onChange={handleToggle(value)}
                checked={checked.indexOf(value) !== -1}
                inputProps={{ 'aria-labelledby': labelId }}
              />
            <ListItemButton>
              <img
                src={`${paths[value].replace("\\", "/")}`}
                srcSet={`${paths[value].replace("\\", "/")}`}
                loading="lazy"
              />
            </ListItemButton>
          </ImageListItem>
      );
    })}
  <Button variant="contained" onClick={(event) => submitForm(event)}>Submit Images to generate Captions</Button>
  </ImageList></div>;
  }

  if(page == "show_text") {
    output = <div>
      <ImageList>
      {list.map((value) => {
        const labelId = `checkbox-list-avatar-label-${value}`;
        return (
          <ImageListItem>
            <ListItemButton>
                <ListItemAvatar>
                  <Avatar
                    alt={`Avatar`}
                    src={`${paths[value].replace("\\", "/")}`}
                  />
                </ListItemAvatar>
              <ListItemText style={{"padding-left": 40 + "px"}}>{captions[value]}</ListItemText>
            </ListItemButton>
          </ImageListItem>
        )
      })}
      </ImageList>
      <p style={{"text-align": "justify", "padding": "40px", "font-size": "20px"}}>{image_captions}</p>
    </div>
  }

  return (<div>
      {output}
      </div>
  );
}
